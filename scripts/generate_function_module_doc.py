    doc = Document()
    set_doc_font(doc)

    doc.add_heading("熙心健康体检平台功能模块详细描述表", level=0)
    p = doc.add_paragraph("说明：本文档依据 ")
    p.add_run("需求规约(1)").bold = True
    p.add_run(" 中 3.2 功能性需求的原表结构重生成，首表已按你确认的功能模块划分替换，后续明细表均与该总表保持一致。")

